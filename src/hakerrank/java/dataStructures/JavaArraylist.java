package hakerrank.java.dataStructures;


import java.util.Scanner;
import java.util.stream.IntStream;

public class JavaArraylist {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        for (int i = 0; i < n; i++) {
            int[] s = new int[n];
            s[i] = sc.nextInt();
        }
    }




}


